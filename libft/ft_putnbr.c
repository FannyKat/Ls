#include "libft.h"

void	ft_putnbr(int n)
{
	long	nb;

	nb = (int)n;
	if (n < 0)
	{
		ft_putchar('-');
		nb = -nb;
	}	
	if (n > 9)
		ft_putnbr(nb / 10);
	ft_putchar(nb % 10 + '0');
}
